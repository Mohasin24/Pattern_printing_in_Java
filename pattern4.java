import java.lang.*;

class pattern4 
{
   public static void main(String arg[])
   {
    int n = 4;

    for(int i = 0; i<n; i++)
    {
        for(int j = i; j<n; j++)
        {
            System.out.print("* ");
        }
        System.out.println();
    }
   } 
}
